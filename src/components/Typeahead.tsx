import { useState } from "react"
import { Check, ChevronsUpDown } from "lucide-react"
 
import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import {
  Command,
  CommandEmpty,
  CommandInput,
  CommandItem,
  CommandList,
} from "@/components/ui/command"
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover"
 
const frameworks = [
  {
    value: "next.js",
    label: "Next.js",
  },
  {
    value: "sveltekit",
    label: "SvelteKit",
  },
  {
    value: "nuxt.js",
    label: "Nuxt.js",
  },
  {
    value: "remix",
    label: "Remix",
  },
  {
    value: "astro",
    label: "Astro",
  },
]
 
export default function TypeaheadSelect() {
  const [open, setOpen] = useState(false)
  const [options, setOptions] = useState(frameworks);
  const [value, setValue] = useState("")
 
  const handleSelect = (selectedValue: string) => {
    if (!options.find(option => option.value == selectedValue)) {
      setOptions([...options, { value: selectedValue, label: selectedValue }]); // Add new value to options
    }
    setValue(selectedValue);
    setOpen(false);
  };

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className="w-[200px] justify-between"
        >
          {value
            ? options.find((framework) => framework.value === value)?.label
            : "Select PLACEHOLDER..."}
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[200px] p-0">
        <Command children={[
          
          <CommandInput
            placeholder="Search or type..."
            value={value}
            onValueChange={setValue}
          />,

          <CommandList>
            <CommandEmpty>
              <Button
                variant="ghost"
                className="w-full"
                onClick={() => handleSelect(value)}
              >
                Create "{value}"
              </Button>
              No framework found.</CommandEmpty>
              {options.map((option) => (
                <CommandItem
                  key={option.value}
                  value={option.value}
                  onSelect={(currentValue: string) => {
                    setValue(currentValue === value ? "" : currentValue)
                    setOpen(false)
                  }}
                >
                  <Check
                    className={cn(
                      "mr-2 h-4 w-4",
                      value === option.value ? "opacity-100" : "opacity-0"
                    )}
                  />
                  {option.label}
                </CommandItem>
              ))}
          </CommandList>,

        ]}/>
      </PopoverContent>
    </Popover>
    );
  }